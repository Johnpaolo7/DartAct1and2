// Simple Dart Calculator
void main () {
  int num1 = 243;
  int num2 = 7;
 
  int sum = num1 + num2;
  int diff = num1 - num2;
  int mul = num1 * num2;
  double div = num1 / num2;
 
  print("Sum: $num1 + $num2 = $sum");
  print("dif: $num1 - $num2 = $diff");
  print("mul: $num1 + $num2 = $mul");
  print("div: $num1 + $num2 = $div");
}